function dalsiKrok(elementID1, elementID2)
{
	document.getElementById(elementID2).style.display = 'block';
	document.getElementById(elementID1).style.display = 'none';			
}

function predchoziKrok(elementID1, elementID2)
{
	document.getElementById(elementID2).style.display = 'block';
	document.getElementById(elementID1).style.display = 'none';			
}